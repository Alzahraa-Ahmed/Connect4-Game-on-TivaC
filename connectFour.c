int possible(int board[R][C], int column){
    int i;
    int occupied = 0;
 
    if(column > C || column < 0 ) {
        return 0;
    }
 
    for(i = 0; i < R; i++) {
        if(board[i][column] != 0)
            occupied++;
    }
 
    if(occupied == R) {
        return 0;
    }
 
    else
        return 1;
}
 

int put(int board[R][C], int column, int player) {
 
    int i;
 
    for(i = 0; i < R; i++) {
        if(board[i][column] == 0) {
            board[i][column] = player;
            return i+1;
        }
    }
		return -1;
}
 
void rmv(int board[6][7], int column) {
    int i;
    for (i=R-1; i>=0; i--){
        if (board[i][column] != 0) {
            board[i][column] = 0;
            break;
        }
    }
}
 
//0=no, 1=player, 2=computer, 3=stalemate
int endgame(int s[6][7],int player) {
    int i, j;

    //check horizontals
    for(i=0; i<6; i++)
        for(j=0; j<=7-4; j++){
            if(s[i][j]== player && s[i][j+1]== player && s[i][j+2]== player && s[i][j+3]== player)
                return player;
        }
 
    //check verticals
    for(i=0; i<=6-4; i++)
        for(j=0; j<7; j++){
            if(s[i][j]== player && s[i+1][j]== player && s[i+2][j]== player && s[i+3][j]== player )
                return player;
        }
 
    //check main diagonals (\)
    for(i=6-1; i>=4-1; i--)
        for(j=0; j<=7-4; j++){
            if(s[i][j]== player && s[i-1][j+1]== player && s[i-2][j+2]== player && s[i-3][j+3]== player)
                return player;
        }
 
    //check other diagonals (/)
    for(i=0; i<=6-4; i++)
        for(j=0; j<=7-4; j++){
            if(s[i][j]== player && s[i+1][j+1]== player && s[i+2][j+2]== player && s[i+3][j+3]== player)
                return player;
        }
 
 
    //check if stalement
    for(i=0; i<7; i++)
        if(s[6-1][i]==0)
            return 0; //game haven't finished yet - there's at least one empty cell in a top of a row
 
    return 3; //stalemate - board is full
}
  
int heuristic(int s[6][7]) {
    int result = 0;
    int i, j;
 
    //check horizontals
    for(i=0; i<6; i++)
        for(j=0; j<=7-4; j++){
            if(s[i][j]!= 2 && s[i][j+1]!= 2 && s[i][j+2]!= 2 && s[i][j+3]!= 2)
                result++;
            if(s[i][j]!= 1 && s[i][j+1]!= 1 && s[i][j+2]!= 1 && s[i][j+3]!= 1)
                result--;
        }
 
    //check verticals
    for(i=0; i<=6-4; i++)
        for(j=0; j<7; j++){
            if(s[i][j]!= 2 && s[i+1][j]!= 2 && s[i+2][j]!= 2 && s[i+3][j]!= 2 )
                result++;
            if(s[i][j]!= 1 && s[i+1][j]!= 1 && s[i+2][j]!= 1 && s[i+3][j]!= 1 )
                result--;
        }
 
    return result;
}
 
int minimax(int board[R][C], int depth, int turn /*1 or 2*/) {
    int e;
    int col, best;
    int n;
		e=endgame(board, 3-turn) ;
    if(e) {
        if(e==3)
            return 0;
        if(e==turn)
            return 10000;
        else
            return -10000;
    }
 
    if(depth==0)
        return ((turn==1) ? heuristic(board) : -heuristic(board));
 
    best = -10000;
 
    for(col=0; col < 7; col++)     //check every move
        if(board[6-1][col]==0) {   //make sure column isn't empty
            put(board, col, turn);
            n = minimax(board, depth-1, 3-turn);
            if(turn==1) {
                if ( -n > best ) best = -n;
            } else { //turn==2
                if ( -n > best ) best = -n;
            }
            rmv(board, col);
        }
 
    return best;
}
 
int ai(int board[R][C], int depth) {

	

    int col, move;
    int n, val = -10000-1;
 
    for(col=0; col < 7; col++)
        if(board[6-1][col]==0) {
            put(board, col, 2);
            n = minimax(board, depth, 1);
            if ( -n > val ) {
                val = -n;
                move = col;
            }
            rmv(board, col);
        }
 
    return move;
}



